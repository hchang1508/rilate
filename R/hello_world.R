#' Print a friendly greeting
#'
#' @return Invisibly returns the printed string `"Hello, world!"`.
#' @export
#'
#' @examples
#' hello_world()
hello_world <- function() {
    print("Hello, world!")
}

#' Print a second friendly greeting
#'
#' @return Invisibly returns the printed string `"Hello, world2!"`.
#' @export
#'
#' @examples
#' hello_world2()
hello_world2 <- function() {
    print("Hello, world2!")
}